document.querySelector(".btn-roll1").addEventListener("click", function () {
    var diceDOM = document.querySelector(".Pic");
    diceDOM.src = "Layer 12.jpg";
   for(var i =1;i<=12;i++){
    var b= document.querySelector(".btn-roll"+i)
       if(i== 1){
        b.style.backgroundColor = "purple"
        b.id="active"
       }
       else{
        b.style.backgroundColor = ""
        b.id="bg"
       }
   }
   document.getElementById("new").style.backgroundColor="purple"
   document.getElementById("new1").style.backgroundColor="purple"
  });
